#include "libft.h"

t_list	*ft_lstnew(char *content, int type)
{
	t_list	*new;

	new = malloc(sizeof(t_list));
	if (new == NULL)
		return (NULL);
	new->content = content;
	new->type = type;
	new->quote_exc = 0;
	new->next = NULL;
	return (new);
}
